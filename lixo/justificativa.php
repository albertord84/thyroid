<h3>Justificativa</h3>

  <p align="justify">
 A Parceria entre as IES participantes se justifica completamente porque
            o Projeto visa a realização de pesquisas em conjunto
            pelas Universidade Federal Fluminense (UFF) e Universidade Federal
            de Pernambuco (UFPE) aplicadas ao estudo (análise e processamento)
            de imagens da mama oriundas de diferentes exames como ultrassonografias,
            mamografias e imagens termográficas. Para tanto, serão
            utilizados recursos humanos e a infra-estrutura disponíveis
            nas duas IES, possibilitando a produção de pesquisas
            científicas e tecnológicas e a formação
            de recursos humanos ao nível de pós-graduação,
            contribuindo, assim, para desenvolver e consolidar o desenvolvimento
            das áreas de computação visual, processamento
            de imagens, modelagem e simulação computacional das
            duas instituições.
