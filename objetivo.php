            <li>Correlacionar os resultados da termografia com as características encontradas na ultrassonografia de tireoide;</li>
            <li>Correlacionar os resultados da termografia com os do exame citopatológico dos nódulos obtidos através da Punção Aspirativa por Agulha Fina (PAAF);</li>
            <li>Correlacionar os resultados da termografia com os do histopatológico, nos casos submetidos à cirurgia;</li>
            <li>Identificar parâmetros na termografia que possam sugerir malignidade ou benignidade em nódulos tireoidianos.</li>
