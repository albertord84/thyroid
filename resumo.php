<h3>Resumo</h3>
    <p align="justify">
        Nódulos tireoidianos são muito frequentes na população em geral. De acordo com estudos de base
        populacional realizados com adultos em áreas com insuficiência de iodo, aproximadamente 4 a 7% de 
        mulheres e 1% dos homens apresentam nódulos palpáveis na tireoide. No entanto, a prevalência de nódulos
        estimada por exames ultrassonográficos é substancialmente maior, chegando a até 68% da população. A importância 
        clínica da investigação dos nódulos da tireoide recai sobre a necessidade de excluir a possibilidade de câncer 
        da tireoide, que ocorre em 7-15% dos casos dependendo da idade, sexo, história de exposição à radiação, histórico 
        familiar, dentre outros fatores.
    </p>

    <p align="justify">
        A termografia é um método de obtenção de imagens térmicas de objetos que estejam numa temperatura 
        acima do zero absoluto. No termograma, a tireoide normal não aparece. No entanto, 
        quando em hipermetabolismo, seja nodular ou difuso, devido ao aumento da angiogênese e do fluxo
        sanguíneo, há emissão de calor facilmente detectável através da pele.
    </p>
    
    <p align="justify">
        Este banco de dados contêm imagens e dados clínicos e histopatológicos recoletados de pacientes com nódulos tireoidianos 
        recrutados nos Ambulatórios de Endocrinologia e Cirurgia do Hospital Universitário Antônio Pedro-HUAP da Universidade 
        Federal Fluminense-UFF em consultas de rotina, submetidos a uma avaliação clínica. A realização da termografia e seus 
        resultados são comparados com os exames de ultrassonografia (US) de tireoide, citopatológico obtido através da punção 
        aspirativa por agulha fina (PAAF) e do histopatológico daqueles que forem submetidos à cirurgia.
    </p>
    
    <p align="justify">
        Um dos objetivos com o banco de dados é avaliar a importância do uso da termografia na
        investigação de nódulos tireoidianos e o seu real papel na identificação de nódulos malignos.
        A termografia é um exame de baixo custo, fácil realização, sem riscos, e sua realização pode ser um instrumento 
        adicional na identificação de nódulos tireoidianos suspeitos de malignidade. Todos os pacientes
        envolvidos no estudo assinaram o Termo de Consentimento Livre e Esclarecido (TCLE).
    </p>

